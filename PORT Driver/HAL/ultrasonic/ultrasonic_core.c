uint32 ultrasonic_calculateDistance(void){
	uint32 calculated_time=0;
	uint32 calculated_Distance =0;
	calculated_time =ICU_CAPTURETIME();
	calculated_Distance =((calculated_time*0.034)/2); 
	return calculated_Distance;
}

